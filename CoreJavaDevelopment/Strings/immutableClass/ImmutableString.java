package com.evergent.corejava.Strings.immutableClass;


public class ImmutableString 
{
    private final String value;
    public ImmutableString(String value)
    {
    	this.value=value;
    }
    public String myvalue()
    {
    	return value;
    }
    public String toString()
    {
    	return value;
    }
}
